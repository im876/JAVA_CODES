package Day1;

public class VariablesDemo {

	@SuppressWarnings("unused")
	public static void main(String[] args) {

		//Local Variables
		int a1 = 56;
		int a2 = 100, a3 = 200;
		
		int sum = a1+a2;
		
		System.out.println("Sum: " + sum);
		
		System.out.println(a1+a2);
		
		System.out.println("Sum: " + (a1 + a2));
		
		//Declaration
		int a4, a5, a6;
		
		//Initialize
		a4 = a5 = a6 = 10;
		
		System.out.println(a4+a5+a6);
		
		byte b1 = 45;
		
		short s1 = 4897;
		
		long l1 = 9876543210l;		
		
		float f1 = 45.36f;
		
		double d1 = 78.36;
		
		boolean b2 = false;
		
		char ch = 'j';	
		

	}

}
