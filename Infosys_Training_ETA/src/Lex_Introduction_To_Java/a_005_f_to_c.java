public class a_005_f_to_c 
{
	public static void main(String[] args)
	{
		short F1 = 32;
		short F2 = 50;
		float C1 = ((F1-32)/9)*5;
		float C2 = ((F2-32)/9)*5;
		System.out.println(C1);
		System.out.println(C2);
	}

}


